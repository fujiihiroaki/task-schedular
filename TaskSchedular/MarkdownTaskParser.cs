using System.Globalization;
using System.Text.RegularExpressions;

#endregion

namespace Jiifureit.TaskSchedular;

/// <summary>
/// Markdownファイルを解析してタスクアイテムを抽出するパーサークラス。
/// チェックリスト形式のタスクとメタデータ（due, start, lead, pace, p, est, tag）を解析します。
/// </summary>
internal static partial class MarkdownTaskParser
{
    // - [ ] xxx
    // - [x] xxx
    private static readonly Regex TaskLine = new(@"^\s*[-*]\s+\[(?<done>[ xX])\]\s+(?<body>.+?)\s*$",
        RegexOptions.Compiled);

    // meta: due/start/lead/pace/p/est/tag
    private static readonly Regex MetaToken = new(@"\b(?<key>due|start|lead|pace|p|est|tag)\s*:\s*(?<val>[^\s]+)\b",
        RegexOptions.Compiled | RegexOptions.IgnoreCase);

    private static readonly HashSet<string> DefaultPaceNames = new(StringComparer.Ordinal)
    {
        "nonstop",
        "rapid",
        "fast",
        "normal",
        "slow"
    };

    public const int MaxPaceDays = 2048;

    private static readonly Regex PeriodLine = new(
        @"^\s*period\s*:\s*(?<start>\d{4}-\d{2}-\d{2})\s*\.\.\s*(?<end>\d{4}-\d{2}-\d{2})\s*$",
        RegexOptions.Compiled | RegexOptions.IgnoreCase);

    private static readonly Regex Heading = new(@"^\s{0,3}#{1,6}\s+(?<text>.+?)\s*$", RegexOptions.Compiled);

    private static readonly Regex DateInHeading = new(@"\b(?<date>\d{4}-\d{2}-\d{2})\b", RegexOptions.Compiled);

    /// <summary>
    /// Markdown文字列を解析してタスクアイテムのリストを生成します。
    /// </summary>
    /// <param name="markdown">解析対象のMarkdown文字列</param>
    /// <returns>解析されたタスクアイテムのリスト</returns>
    public static List<TaskItem> Parse(string markdown)
    {
        var tasks = new List<TaskItem>();
        string? currentSection = null;
        DateTime? currentSectionDate = null;
        DateTime? currentPeriodEnd = null;

        foreach (var line in _ReadLines(markdown))
        {
            var period = PeriodLine.Match(line);
            if (period.Success)
            {
                if (_TryParseDateValue(period.Groups["start"].Value, out _)
                    && _TryParseDateValue(period.Groups["end"].Value, out var end))
                {
                    currentPeriodEnd = _ToEndOfDay(end);
                }

                continue;
            }

            var h = Heading.Match(line);
            if (h.Success)
            {
                currentSection = h.Groups["text"].Value.Trim();
                currentSectionDate = _TryParseDateFromHeading(currentSection);
                continue;
            }

            var m = TaskLine.Match(line);
            if (!m.Success) continue;

            var isDone = m.Groups["done"].Value is "x" or "X";
            var body = m.Groups["body"].Value.Trim();

            var (title, meta) = _SplitTitleAndMeta(body);

            var item = new TaskItem
            {
                RawLine = line,
                Title = title,
                IsDone = isDone,
                Section = currentSection,
                SectionDate = currentSectionDate,
                PeriodEnd = currentPeriodEnd,
                Id = _MakeStableId(title, meta)
            };

            _ApplyMeta(item, meta);

            // due がなければ見出し日付を採用（1〜2週間の運用に強い）
            item.Due ??= item.SectionDate;

            // lead不要対応：start推定
            Inference.ApplyAutoStart(item);

            tasks.Add(item);
        }

        return tasks;
    }

    /// <summary>
    /// Markdown文字列からタスクIDを抽出します。HTMLコメント形式（&lt;!-- id:xxx --&gt;）で埋め込まれたIDを取得します。
    /// </summary>
    /// <param name="markdown">検索対象のMarkdown文字列</param>
    /// <returns>抽出されたタスクIDのセット</returns>
    public static HashSet<string> ExtractTaskIds(string markdown)
    {
        var set = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        foreach (var line in _ReadLines(markdown))
        {
            var idx = line.IndexOf("<!-- id:", StringComparison.OrdinalIgnoreCase);
            if (idx < 0) continue;

            var end = line.IndexOf("-->", idx, StringComparison.OrdinalIgnoreCase);
            if (end < 0) continue;

            var inside = line.Substring(idx + "<!-- id:".Length, end - (idx + "<!-- id:".Length));
            var id = inside.Trim();
            if (!string.IsNullOrWhiteSpace(id))
                set.Add(id);
        }

        return set;
    }

    /// <summary>
    /// 文字列を行ごとに分割して返します。
    /// </summary>
    /// <param name="s">分割対象の文字列</param>
    /// <returns>行のシーケンス</returns>
    private static IEnumerable<string> _ReadLines(string s)
    {
        using var sr = new StringReader(s);
        while (sr.ReadLine() is { } line)
        {
            yield return line;
        }
    }

    /// <summary>
    /// 見出しテキストから日付を抽出します（yyyy-MM-dd形式）。
    /// </summary>
    /// <param name="headingText">見出しテキスト</param>
    /// <returns>抽出された日付。見つからない場合はnull</returns>
    private static DateTime? _TryParseDateFromHeading(string headingText)
    {
        var m = DateInHeading.Match(headingText);
        if (!m.Success) return null;

        if (_TryParseDateValue(m.Groups["date"].Value, out var d))
            return _ToEndOfDay(d);

        return null;
    }

    private static bool _TryParseDateValue(string value, out DateTime date)
    {
        return DateTime.TryParseExact(value,
            "yyyy-MM-dd",
            CultureInfo.InvariantCulture,
            DateTimeStyles.None,
            out date);
    }

    /// <summary>
    /// 日付を当日の23:59:59.999に変換します（締切日処理用）。
    /// </summary>
    /// <param name="date">変換元の日付</param>
    /// <returns>23:59:59.999に設定された日付</returns>
    private static DateTime _ToEndOfDay(DateTime date)
    {
        return date.Date.AddDays(1).AddMilliseconds(-1);
    }

    /// <summary>
    /// タスク本文からタイトルとメタデータを分離します。
    /// </summary>
    /// <param name="body">タスク本文</param>
    /// <returns>タイトルとメタデータのタプル</returns>
    private static (string title, string meta) _SplitTitleAndMeta(string body)
    {
        var tokens = MetaToken.Matches(body);
        if (tokens.Count == 0) return (body, "");

        var earliest = tokens[0].Index;
        for (var i = 1; i < tokens.Count; i++)
        {
            earliest = Math.Min(earliest, tokens[i].Index);
        }

        var title = body[..earliest].TrimEnd();
        var meta = body[earliest..].Trim();
        return (title, meta);
    }

    /// <summary>
    /// メタデータ文字列を解析してTaskItemにプロパティを設定します。
    /// </summary>
    /// <param name="item">設定対象のTaskItem</param>
    /// <param name="meta">メタデータ文字列</param>
    private static void _ApplyMeta(TaskItem item, string meta)
    {
        foreach (Match m in MetaToken.Matches(meta))
        {
            var key = m.Groups["key"].Value.ToLowerInvariant();
            var val = m.Groups["val"].Value;

            switch (key)
            {
                case "due":
                    if (DateTime.TryParseExact(val,
                            "yyyy-MM-dd",
                            CultureInfo.InvariantCulture,
                            DateTimeStyles.None,
                            out var d))
                        item.Due = _ToEndOfDay(d);
                    break;

                case "start":
                    if (DateTime.TryParseExact(val,
                            "yyyy-MM-dd",
                            CultureInfo.InvariantCulture,
                            DateTimeStyles.None,
                            out var s))
                        item.Start = s.Date;
                    break;

                case "lead":
                    if (_TryParseLead(val, out var lead))
                        item.Lead = lead;
                    break;

                case "pace":
                    if (_TryNormalizePace(val, out var normalizedPace, out var paceError))
                        item.Pace = normalizedPace;
                    else
                        _WriteInvalidPaceWarning(item, val, paceError);
                    break;

                case "p":
                    if (int.TryParse(val, NumberStyles.Integer, CultureInfo.InvariantCulture, out var p))
                        item.ManualPriority = p;
                    break;

                case "est":
                    if (_TryParseEstimate(val, out var ts))
                        item.Estimate = ts;
                    break;

                case "tag":
                    item.Tags.Add(val.Trim());
                    break;
            }
        }

        // leadが明示されていて start が空なら、ここではまだ確定しない
        // (Inference で due が決まったあとに start を作るため)
    }

    private static bool _TryNormalizePace(string raw, out string normalized, out string error)
    {
        normalized = "";
        error = "";

        var value = raw.Trim();
        if (string.IsNullOrWhiteSpace(value))
        {
            error = "empty pace value";
            return false;
        }

        var eqIdx = value.IndexOf('=');
        if (eqIdx < 0)
        {
            var name = value.ToLowerInvariant();
            if (!DefaultPaceNames.Contains(name))
            {
                error = "unknown pace name";
                return false;
            }

            normalized = name;
            return true;
        }

        var namePart = value[..eqIdx].Trim().ToLowerInvariant();
        var daysPart = value[(eqIdx + 1)..].Trim();

        if (!_MyRegex3().IsMatch(namePart))
        {
            error = "invalid pace name";
            return false;
        }

        if (!int.TryParse(daysPart, NumberStyles.Integer, CultureInfo.InvariantCulture, out var days))
        {
            error = "days must be an integer";
            return false;
        }

        if (days is < 1 or > MaxPaceDays)
        {
            error = $"days out of range 1..{MaxPaceDays}";
            return false;
        }

        normalized = $"{namePart}={days.ToString(CultureInfo.InvariantCulture)}";
        return true;
    }

    private static void _WriteInvalidPaceWarning(TaskItem item, string rawValue, string reason)
    {
        Console.Error.WriteLine(
            $"[pace] invalid value '{rawValue}' (reason: {reason}, id:{item.Id}, title:{item.Title})");
    }

    /// <summary>
    /// 見積もり時間文字列（例: 30m, 2h, 1d）をTimeSpanに変換します。
    /// </summary>
    /// <param name="s">見積もり時間文字列</param>
    /// <param name="ts">変換されたTimeSpan</param>
    /// <returns>変換に成功した場合true</returns>
    private static bool _TryParseEstimate(string s, out TimeSpan ts)
    {
        // est: 30m / 2h / 1d（d=8h換算）
        ts = TimeSpan.Zero;
        s = s.Trim().ToLowerInvariant();
        var m = Regex.Match(s, @"^(?<n>\d+)(?<u>m|h|d)$");
        if (!m.Success) return false;

        var n = int.Parse(m.Groups["n"].Value, CultureInfo.InvariantCulture);
        var u = m.Groups["u"].Value;

        ts = u switch
        {
            "m" => TimeSpan.FromMinutes(n),
            "h" => TimeSpan.FromHours(n),
            "d" => TimeSpan.FromHours(n * 8),
            _ => TimeSpan.Zero
        };
        return true;
    }

    /// <summary>
    /// リードタイム文字列（例: 90d, 12w, 3m, 1y）をTimeSpanに変換します。
    /// </summary>
    /// <param name="s">リードタイム文字列</param>
    /// <param name="ts">変換されたTimeSpan</param>
    /// <returns>変換に成功した場合true</returns>
    private static bool _TryParseLead(string s, out TimeSpan ts)
    {
        // lead: 90d / 12w / 3m（m=30d）/ 1y（365d）
        ts = TimeSpan.Zero;
        s = s.Trim().ToLowerInvariant();
        var m = _MyRegex2().Match(s);
        if (!m.Success) return false;

        var n = int.Parse(m.Groups["n"].Value, CultureInfo.InvariantCulture);
        var u = m.Groups["u"].Value;

        ts = u switch
        {
            "d" => TimeSpan.FromDays(n),
            "w" => TimeSpan.FromDays(n * 7),
            "m" => TimeSpan.FromDays(n * 30),
            "y" => TimeSpan.FromDays(n * 365),
            _ => TimeSpan.Zero
        };
        return true;
    }

    /// <summary>
    /// タイトルとメタデータから安定したハッシュIDを生成します。
    /// </summary>
    /// <param name="title">タスクタイトル</param>
    /// <param name="meta">メタデータ文字列</param>
    /// <returns>8桁の16進数ハッシュ文字列</returns>
    private static string _MakeStableId(string title, string meta)
    {
        var basis = (title + "|" + meta).Trim().ToLowerInvariant();
        basis = _MyRegex1().Replace(basis, " ");
        unchecked
        {
            var hash = basis.Aggregate(2166136261, (current, ch) => (current ^ ch) * 16777619);
            return hash.ToString("x8", CultureInfo.InvariantCulture);
        }
    }

    [GeneratedRegex(@"\s+")]
    private static partial Regex _MyRegex1();

    [GeneratedRegex(@"^(?<n>\d+)(?<u>d|w|m|y)$")]
    private static partial Regex _MyRegex2();

    [GeneratedRegex(@"^[a-z][a-z0-9_-]*$")]
    private static partial Regex _MyRegex3();
}